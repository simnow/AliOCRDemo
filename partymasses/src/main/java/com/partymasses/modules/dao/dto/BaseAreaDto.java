package com.partymasses.modules.dao.dto;

import java.util.List;

//三级列表类
public class BaseAreaDto {
	
	private String value;//gid编码
	
	private String label;//gname名称
	
	private List<BaseAreaDto> children;

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

	public String getLabel() {
		return label;
	}

	public void setLabel(String label) {
		this.label = label;
	}

	public List<BaseAreaDto> getChildren() {
		return children;
	}

	public void setChildren(List<BaseAreaDto> children) {
		this.children = children;
	}
	
}
