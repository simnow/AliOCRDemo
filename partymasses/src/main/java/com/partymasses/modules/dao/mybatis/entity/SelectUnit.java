package com.partymasses.modules.dao.mybatis.entity;

import java.util.Date;
import java.util.List;

import org.springframework.stereotype.Component;
@Component
public class SelectUnit {
	private String unitname;
	private String unitdistrict;
	private String begintime;
	private String finishtime;
	private List<Integer> paging;
	
	public List<Integer> getPaging() {
		return paging;
	}
	public void setPaging(List<Integer> paging) {
		this.paging = paging;
	}
	public String getUnitname() {
		return unitname;
	}
	public void setUnitname(String unitname) {
		this.unitname = unitname;
	}
	public String getUnitdistrict() {
		return unitdistrict;
	}
	public void setUnitdistrict(String unitdistrict) {
		this.unitdistrict = unitdistrict;
	}
	public String getBegintime() {
		return begintime;
	}
	public void setBegintime(String begintime) {
		this.begintime = begintime;
	}
	public String getFinishtime() {
		return finishtime;
	}
	public void setFinishtime(String finishtime) {
		this.finishtime = finishtime;
	}
	public SelectUnit(String unitname, String unitdistrict, String begintime, String finishtime, List<Integer> paging) {
		super();
		this.unitname = unitname;
		this.unitdistrict = unitdistrict;
		this.begintime = begintime;
		this.finishtime = finishtime;
		this.paging = paging;
	}
	public SelectUnit() {
		super();
	}
	
}
